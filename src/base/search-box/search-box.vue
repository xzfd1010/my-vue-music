<template>
  <div class="search-box">
    <i class="icon-search"></i>
    <input type="text" class="box" :placeholder="placeholder" v-model="query" ref="query">
    <i @click="clear" class="icon-dismiss" v-show="query"></i>
  </div>
</template>

<script>
  import {debounce} from 'common/js/util'

  export default {
    props: {
      placeholder: {
        type: String,
        default: '搜索歌曲、歌手'
      }
    },
    data() {
      return {
        query: ''
      }
    },
    methods: {
      clear() {
        this.query = ''
      },
      setQuery(query) {
        this.query = query
      },
      blur() {
        this.$refs.query.blur()
      }
    },
    created() {
      // 为什么用这种方式？需要节流
      // 这里是将子组件内容传递给父组件
      this.$watch('query', debounce((newQuery) => {
        this.$emit('query', newQuery)
      }, 200)) // 200ms内只会触发一次
    }
  }
</script>

